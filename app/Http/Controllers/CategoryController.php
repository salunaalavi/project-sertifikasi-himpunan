     * Update the specified resource in storage.
     *
     * @param  \App\Http\Requests\UpdateCategoryRequest  $request
     * @param  \App\Models\Category  $category
     * @return \Illuminate\Http\Response
     */
    public function update(UpdateCategoryRequest $request, Category $category)
    {
        $validatedData = $request->validate([
            'deskripsi' => 'required',
            'nama_gambar' => 'required|image'
        ]);
        
        Storage::delete($request->old_nama_gambar);
        $validatedData['nama_gambar'] = $request->file('nama_gambar')->store('category-images');
        $validatedData['id_tipe'] = $category->id_tipe;
        $validatedData['tipe'] = $category->tipe;

        Category::where('id', $category->id)->update($validatedData);

        return redirect('/categories')->with('berhasil', 'Kategori berhasil diubah!');
    }

